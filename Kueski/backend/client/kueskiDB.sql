#Tablas--------------------------------------------
CREATE TABLE USERS(
	USER_ID INT UNIQUE NOT NULL,
    IDENTITY_USER_ID TINYTEXT,
    USER_NAME TINYTEXT,
    FIRST_LAST_NAME TINYTEXT,
    SECOND_LAST_NAME TINYTEXT,
    BORN_DATE DATE,
    NACIONALITY TINYTEXT,
    STATE_OF_BIRTH TINYTEXT,
    ECONOMIC_ACTIVITY TINYTEXT,
    CURP TINYTEXT,
    PHONE_NUMER TINYTEXT,
    EMAIL TINYTEXT,
    USER_DATA JSON,
    IS_CLIENT BOOLEAN,
    IS_BLOCKED BOOLEAN,
    CREATED_AT TIMESTAMP,
    UPDATE_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
	
    PRIMARY KEY(USER_ID)
);

CREATE TABLE ADDRESSES(
	ADRESSES_KEY INT NOT NULL AUTO_INCREMENT,
    USER_ID INT,
    COUNTRY TINYTEXT,
    STATE TINYTEXT,
    CITY TINYTEXT,
    NEIGHBORHOOD TINYTEXT,
    ZIP_CODE INT,
    STREET TINYTEXT,
    EXT_NUMBER INT,
    INT_NUMBER INT,
    CREATED_AT TIMESTAMP,
    UPDATE_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
    
    PRIMARY KEY(ADRESSES_KEY),
    FOREIGN KEY(USER_ID) REFERENCES USERS(USER_ID)
);

CREATE TABLE ARCO_REQUEST(
	ID_REQUEST INT NOT NULL AUTO_INCREMENT,
    USER_ID INT,
    TYPE_REQUEST INT,
    COMMENTARIOS TINYTEXT,
    PREV_VALUE TINYTEXT,
    NEW_VALUE TINYTEXT,
    FECHA TIMESTAMP,
    
    PRIMARY KEY(ID_REQUEST),
    FOREIGN KEY(USER_ID) REFERENCES USERS(USER_ID)
);

CREATE TABLE IDENTIFICATIONS(
	ID_KEY INT NOT NULL AUTO_INCREMENT,
    USER_ID INT,
    IDENTIFICATION_TYPE TINYTEXT,
    IDENTIFICATION_NUMBER TINYTEXT,
    CREATED_AT TIMESTAMP,
    UPDATE_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
    
    PRIMARY KEY(ID_KEY),
    FOREIGN KEY(USER_ID) REFERENCES USERS(USER_ID)
);

CREATE TABLE OPOSICION(
	OPPOSITION_KEY INT NOT NULL AUTO_INCREMENT,
    USER_ID INT, 
    OPPOSITION_TYPE INT,
	CREATED_AT TIMESTAMP,
    UPDATE_AT TIMESTAMP,
    DELETED_AT TIMESTAMP,
    
	PRIMARY KEY(OPPOSITION_KEY),
    FOREIGN KEY(USER_ID) REFERENCES USERS(USER_ID)
)

