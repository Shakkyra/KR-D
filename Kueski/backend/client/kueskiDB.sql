#Procedimiento para "borrar" los datos------------------------------------
DELIMITER &&
CREATE PROCEDURE ARCO_DELETE_USER(IN VAR TINYTEXT)
BEGIN
    UPDATE ADDRESSES
    SET 
		ADDRESSES.COUNTRY = NULL,
		ADDRESSES.STATE = NULL,
		ADDRESSES.CITY  = NULL,
		ADDRESSES.NEIGHBORHOOD  = NULL,
		ADDRESSES.ZIP_CODE  = NULL,
		ADDRESSES.STREET  = NULL,
		ADDRESSES.EXT_NUMBER  = NULL,
		ADDRESSES.INT_NUMBER  = NULL,
		ADDRESSES.CREATED_AT  = NULL,
		ADDRESSES.UPDATE_AT  = NULL,
		ADDRESSES.DELETED_AT = CURRENT_TIMESTAMP()
    WHERE ADDRESSES.USER_ID = VAR;
    
    UPDATE IDENTIFICATIONS
    SET 
        IDENTIFICATIONS.IDENTIFICATION_TYPE = NULL,
		IDENTIFICATIONS.IDENTIFICATION_NUMBER = NULL,
		IDENTIFICATIONS.CREATED_AT = NULL,
		IDENTIFICATIONS.UPDATE_AT = NULL,
		IDENTIFICATIONS.DELETED_AT = CURRENT_TIMESTAMP()
	WHERE IDENTIFICATIONS.USER_ID = VAR;
    
    UPDATE USERS
    SET 
		USERS.USER_NAME = NULL,
		USERS.FIRST_LAST_NAME = NULL,
		USERS.SECOND_LAST_NAME = NULL,
		USERS.BORN_DATE = NULL,
		USERS.NACIONALITY = NULL,
		USERS.STATE_OF_BIRTH = NULL,
		USERS.ECONOMIC_ACTIVITY = NULL,
		USERS.CURP = NULL,
		USERS.PHONE_NUMER = NULL,
		USERS.EMAIL = NULL,
		USERS.USER_DATA = NULL,
		USERS.IS_CLIENT = NULL,
		USERS.IS_BLOCKED = NULL,
		USERS.CREATED_AT = NULL,
		USERS.UPDATE_AT = NULL,
		USERS.DELETED_AT = CURRENT_TIMESTAMP()
    WHERE 
		USERS.USER_ID = VAR;
END &&
DELIMITER ;
